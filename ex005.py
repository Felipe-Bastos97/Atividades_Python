import math
n=int(input('Digite um valor !'))
n2=int(input('Digite um valor !'))

print('Analisando o valor {}, seu antecessor é {} e seu sucessor é {}'.format(n,(n-1),(n+1)))

print('Analisando o valor {}, é {} a média {}'.format(n,n2,((n+n2)/2)))

print('O dobro de é {}' .format(n*2))
print('O triplo de é {}' .format(n*3))
print('A Raiz de é {:.2f}' .format(math.sqrt(n)))



